// Calcular e apresentar o valor do volume de uma lata de óleo, utilizando a fórmula:
// Volume *Raio 2 * Altura

let Volume
let Raio
let Altura

Raio = parseFloat(prompt("Digite o Raio da lata de oleo :"));

Altura = parseFloat(prompt("Digite a altura da lata de oleo:"));

// Volume =  3.14159

Volume = 3.14159 * Raio * Raio * Altura

alert("O volume da lata de oleo é:" + Volume);





// o volume de uma lata de oleo é: 3.14159